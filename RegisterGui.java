    public boolean Validation() {
        if (Name.getText().isEmpty()) {

            jLabel11.setText("Please Enter Name");
            return false;
        }
        if (Email.getText().isEmpty()) {
            jLabel12.setText("Please Enter Email");
        }
        if (Email.getText().isEmpty()) {
            jLabel12.setText("Please Enter Email");
            return false;
        }
        if (Mobile.getText().isEmpty()) {
            jLabel14.setText("Please Enter Mobile Number");
            return false;
        }
        if (Email.getText().isEmpty()) {
            jLabel12.setText("Please Enter Email");
            return false;
        }
        if (Date1.getDate() == null) {
            jLabel15.setText("Please Select Birthdate");
            return false;
        }
        if (Address.getText().isEmpty()) {
            addlbl.setText("Please Enter Address");
            return false;
        }
        if (buttonGroup1.getSelection() == null) {
            jLabel13.setText("Please Select Gender");
            return false;
        }
        if (Aadharno.getText().isEmpty()) {
            jLabel17.setText("Please Enter Aadhar NO");
            return false;
        }
        if (pass1.getText().isEmpty()) {
            jLabel18.setText("Please Enter Password");
            return false;
        }
        if (pass2.getText().isEmpty()) {
            jLabel20.setText("Please Confirm Password");
            return false;
        }
       if(pass1.getText().equals(pass2.getText()))
        {
          return true;
        }else
       {
           jLabel20.setText("Password Not match");
           return false;
       }
        
    }

