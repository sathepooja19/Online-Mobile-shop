    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Male.setActionCommand("Male");
        Female.setActionCommand("FeMale");
        c = new Customerbean();

        if (Validation() == true) {
            c.setCustomerEmail(Email.getText());
            RegisterDao R = new RegisterDao();
            if (R.checkDuplicate() == true) {

                JOptionPane.showMessageDialog(this, "Email Address Alredy Exist");

            } else if (R.checkDuplicate() == false) {
                setValues();
                R.Register();
                JOptionPane.showMessageDialog(this, "Registration Successfull ");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

